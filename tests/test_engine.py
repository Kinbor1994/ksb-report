"""
Tests for ReportEngine — PDF generation with all element types.
"""

import pytest

from ksb_report.engine import ReportEngine
from ksb_report.schemas import (
    FooterConfig,
    HeaderConfig,
    HeaderLine,
    ReportTemplate,
    WatermarkConfig,
)
from ksb_report.templates.invoice import invoice_template


class TestReportEngine:
    """Test PDF generation via ReportEngine."""

    def setup_method(self):
        self.engine = ReportEngine()

    def _assert_valid_pdf(self, pdf_bytes):
        assert isinstance(pdf_bytes, bytes)
        assert len(pdf_bytes) > 0
        assert pdf_bytes[:5] == b"%PDF-"

    def test_generate_empty_template(self):
        self._assert_valid_pdf(self.engine.generate(ReportTemplate()))

    def test_generate_with_title(self):
        self._assert_valid_pdf(
            self.engine.generate(ReportTemplate(title="Test Report"))
        )

    def test_generate_with_text(self):
        template = ReportTemplate(
            elements=[
                {"type": "text", "content": "Hello World", "font_size": 14},
                {"type": "text", "content": "Second paragraph", "font_style": "I"},
                {"type": "spacer", "height": 10},
                {"type": "text", "content": "After spacer", "align": "C"},
            ],
        )
        self._assert_valid_pdf(self.engine.generate(template))

    def test_generate_with_table(self):
        template = ReportTemplate(
            elements=[
                {
                    "type": "table",
                    "headers": ["Name", "Age", "City"],
                    "rows": [["Alice", "30", "Paris"], ["Bob", "25", "London"]],
                    "header_bg_color": [41, 128, 185],
                },
            ],
        )
        self._assert_valid_pdf(self.engine.generate(template))

    def test_table_with_column_aligns(self):
        template = ReportTemplate(
            elements=[
                {
                    "type": "table",
                    "headers": ["Name", "Score", "Amount"],
                    "rows": [["Alice", "95", "1,500 €"]],
                    "column_aligns": ["L", "C", "R"],
                },
            ],
        )
        self._assert_valid_pdf(self.engine.generate(template))

    def test_generate_with_large_table(self):
        rows = [[f"Item {i}", f"Value {i}", f"Desc {i}"] for i in range(100)]
        template = ReportTemplate(
            elements=[
                {
                    "type": "table",
                    "headers": ["Item", "Value", "Description"],
                    "rows": rows,
                    "max_rows_per_page": 25,
                },
            ],
        )
        pdf = self.engine.generate(template)
        self._assert_valid_pdf(pdf)
        assert len(pdf) > 2000

    def test_section_title_and_key_value(self):
        template = ReportTemplate(
            elements=[
                {"type": "section_title", "text": "Section I"},
                {"type": "key_value", "pairs": [["Key", "Value"]]},
            ],
        )
        self._assert_valid_pdf(self.engine.generate(template))

    def test_header_and_footer(self):
        template = ReportTemplate(
            header=HeaderConfig(
                lines=[HeaderLine(text="My Company", font_size=16, bold=True)],
            ),
            footer=FooterConfig(left_text="Generated by KSB-Report"),
            elements=[{"type": "text", "content": "Content"}],
        )
        self._assert_valid_pdf(self.engine.generate(template))

    def test_generate_to_file(self, tmp_path):
        output = str(tmp_path / "test_output.pdf")
        result = self.engine.generate_to_file(ReportTemplate(title="File Test"), output)
        assert result == output
        with open(output, "rb") as f:
            self._assert_valid_pdf(f.read())

