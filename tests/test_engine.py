    # =========================================================================
    # Invoice template
    # =========================================================================
    def test_invoice_template(self):
        template = invoice_template(
            company_name="KSB Tech",
            invoice_number="2026-001",
            client_name="Jean Dupont",
            items=[
                {"description": "Widget A", "quantity": 2, "unit_price": 15.00},
                {"description": "Service B", "quantity": 1, "unit_price": 50.00},
            ],
            tax_rate=0.18,
        )
        assert template.title == "FACTURE 2026-001"
        pdf = self.engine.generate(template)
        self._assert_valid_pdf(pdf)
        assert len(pdf) > 1000

    # =========================================================================
    # Mixed content
    # =========================================================================
    def test_mixed_all_elements(self):
        """Report using every element type at once."""
        template = ReportTemplate(
            title="Full Demo Report",
            watermark={"text": "DRAFT"},
            variables={"author": "Test Author"},
            header=HeaderConfig(
                lines=[HeaderLine(text="Test Corp", font_size=14, bold=True)],
            ),
            footer=FooterConfig(left_text="Footer — {{author}}"),
            elements=[
                {"type": "text", "content": "By {{author}}"},
                {"type": "separator"},
                {"type": "section_title", "text": "Section A"},
                {
                    "type": "list",
                    "items": ["Point 1", "Point 2"],
                    "list_style": "bullet",
                },
                {"type": "key_value", "pairs": [["Key", "Val"]]},
                {"type": "spacer", "height": 5},
                {
                    "type": "columns",
                    "columns": [
                        {"width": 50, "elements": [{"type": "text", "content": "L"}]},
                        {"width": 50, "elements": [{"type": "text", "content": "R"}]},
                    ],
                },
                {
                    "type": "table",
                    "headers": ["A", "B"],
                    "rows": [["1", "2"]],
                    "column_aligns": ["L", "R"],
                },
                {
                    "type": "box",
                    "elements": [{"type": "text", "content": "Boxed"}],
                },
                {"type": "qrcode", "data": "https://test.com"},
                {"type": "page_break"},
                {
                    "type": "signature_block",
                    "signatures": [
                        {"label": "Signed", "name": "John"},
                    ],
                },
            ],
        )
        pdf = self.engine.generate(template)
        self._assert_valid_pdf(pdf)
        assert len(pdf) > 2000
